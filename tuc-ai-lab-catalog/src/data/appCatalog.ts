/**
 * Get apps by status
 */
export const getAppsByStatus = (status: CatalogApp['status']): CatalogApp[] => {
  return appCatalog.filter((app) => app.status === status);
};

/**
 * Get apps by category
 */
export const getAppsByCategory = (category: CatalogApp['category']): CatalogApp[] => {
  return appCatalog.filter((app) => app.category === category);
};

/**
 * Get completeness score for an app
 */
export const getCompletenessScore = (app: CatalogApp): number => {
  const criteria = [app.vite, app.favicon, app.seo, app.splash, app.deploy];
  return Math.round((criteria.filter(Boolean).length / criteria.length) * 100);
};

/**
 * Get overall standardisation summary
 */
export const getCatalogSummary = () => {
  const standardised = getAppsByStatus('standardised').length;
  const inProgress = getAppsByStatus('in-progress').length;
  const notStarted = getAppsByStatus('not-started').length;

  return {
    total: appCatalog.length,
    standardised,
    inProgress,
    notStarted,
    completeness: Math.round((standardised / appCatalog.length) * 100),
  };
};
